var by_code = {};

for (var i = 0; i < iso_3166.length; ++i) {
    by_code[iso_3166[i].alpha2.toLowerCase()] = i;
    by_code[iso_3166[i].alpha3.toLowerCase()] = i;
    by_code[iso_3166[i].num] = i;
}

/* API */

function frDep(field, value)
{
	for (var i = 0; i < iso_3166_2_fr_dep.length; ++i)
		if (iso_3166_2_fr_dep[i][field] == value)
			return iso_3166_2_fr_dep[i];
	return undefined;
}

function frFromDep(field, value)
{
	var dep = getIso3166FrDep(field, value);

	if (dep == undefined)
		return undefined;

	var reg = getIso3166Fr('alpha', dep.region);

	return reg;
}

function fr(field, value)
{
	for (var i = 0; i < iso_3166_2_fr.length; ++i)
		if (iso_3166_2_fr[i][field] == value)
			return iso_3166_2_fr[i];
	return undefined;
}

function lookup(code)
{
    var index = by_code[code.toString().toLowerCase()];

    if (index !== undefined)
        return iso_3166[index];

    return {
	num: 0,
    alpha3: "",
    alpha2: "",
	name: "Inconnu",
	iso: "INCONNU",
    }
}
